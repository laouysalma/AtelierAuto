     private void loadClient() {
        clients.removeAllItems();
        for (Client c : cs.findAll()) {
            clients.addItem(c);
        }
    }

     public void load() {
        model.setRowCount(0);
        for (Vehicule v : vs.findAll()) {
             Client client = cs.findById(v.getIdClient());
             String clientNom = (client != null) ? client.getNom() + " " + client.getPrenom() : "N/A";
            model.addRow(new Object[]{
                v.getImmatricule(),
                v.getMarque(),
                v.getModele(),
                v.getIdClient(), 
                clientNom  
            });
        }
    }
      public void vider() {
        immatriculetxt.setText("");
        marquetxt.setText("");
        modeletxt.setText("");
        if(clients.getItemCount() > 0) {
            clients.setSelectedIndex(0);
        }
        immatricule = null;
        immatriculetxt.setEnabled(true);
    }

    private int getSelectedClientId() {
        Object selectedItem = clients.getSelectedItem();
        if (selectedItem instanceof Client) {
            return ((Client) selectedItem).getId_client();
        }
        return 0; 
    }
    private void selectClientInCombo(int clientId) {
        for (int i = 0; i < clients.getItemCount(); i++) {
            Object item = clients.getItemAt(i);
            if (item instanceof Client) {
                Client client = (Client) item;
                if (client.getId_client() == clientId) {
                    clients.setSelectedItem(client);
                    return;
                }
            }
            if(clients.getItemCount() > 0) {
            clients.setSelectedIndex(0);
        }
        }
   
    }
