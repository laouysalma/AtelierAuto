    private void DeleteVehiculeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteVehiculeActionPerformed
             if(immatricule == null) {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner un véhicule !");
            return;
        }
        
        int reponse = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment supprimer ce véhicule ?");
        if(reponse == 0) {
            vs.delete(vs.findById(immatricule));
            load();
            vider();
        }
        }//GEN-LAST:event_DeleteVehiculeActionPerformed

    private void AddVehiculeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddVehiculeActionPerformed
     if(immatriculetxt.getText().trim().isEmpty() || 
           marquetxt.getText().trim().isEmpty() || 
           modeletxt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs !");
            return;
        }
        
        if(clients.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner un client !");
            return;
        }
        
        String immatricule = immatriculetxt.getText().trim();
        String marque = marquetxt.getText().trim();
        String modele = modeletxt.getText().trim();
        int idClient = getSelectedClientId();
        
        vs.create(new Vehicule(immatricule, marque, modele, idClient));
        load();
        vider();
    }//GEN-LAST:event_AddVehiculeActionPerformed

    private void listevehiculeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listevehiculeMouseClicked
       int row = listevehicule.getSelectedRow();
        if(row >= 0) {
            immatricule = model.getValueAt(row, 0).toString();
            marquetxt.setText(model.getValueAt(row, 1).toString());
            modeletxt.setText(model.getValueAt(row, 2).toString());
            immatriculetxt.setText(immatricule);
            
            // Récupérer et sélectionner le client
            Object idClientObj = model.getValueAt(row, 3);
            if(idClientObj != null) {
                try {
                    int idClient = Integer.parseInt(idClientObj.toString());
                    selectClientInCombo(idClient);
                } catch(NumberFormatException e) {
                    if(clients.getItemCount() > 0) {
                        clients.setSelectedIndex(0);
                    }
                }
            }
            
            // Désactiver l'immatricule en mode modification
            immatriculetxt.setEnabled(false);
        }
    }//GEN-LAST:event_listevehiculeMouseClicked

    private void UpdateVehiculeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateVehiculeActionPerformed
        if(immatricule == null) {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner un véhicule !");
            return;
        }
        
        int reponse = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment modifier ce véhicule ?");
        if(reponse == 0) {
            String marque = marquetxt.getText().trim();
            String modele = modeletxt.getText().trim();
            int idClient = getSelectedClientId();
            
            vs.update(new Vehicule(immatricule, marque, modele, idClient));
            load();
            vider();
        }
    }//GEN-LAST:event_UpdateVehiculeActionPerformed

