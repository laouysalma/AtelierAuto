    private void AddClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddClientActionPerformed

        if (nomtxt.getText().trim().isEmpty() || prenomtxt.getText().trim().isEmpty() || telephonetxt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tous les champs sont obligatoires !", "Erreur", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            cs.create(new Client(nomtxt.getText(), prenomtxt.getText(), telephonetxt.getText()));
            vider();
            load();
            JOptionPane.showMessageDialog(this, "Client ajouté avec succès !");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur lors de l'ajout : " + e.getMessage(),
                    "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_AddClientActionPerformed

    private void UpdateClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateClientActionPerformed
        try {
            int reponse = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment modifier ce client ?");
            if (reponse == JOptionPane.YES_OPTION) {
                cs.update(new Client(id_client, nomtxt.getText(), prenomtxt.getText(), telephonetxt.getText()));
                vider();
                load();
                JOptionPane.showMessageDialog(this, "Client modifié avec succès !");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur lors de la modification : " + e.getMessage(),
                    "Erreur", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_UpdateClientActionPerformed

    private void DeleteClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteClientActionPerformed
        try {
            int reponse = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment supprimer ce client ?");
            if (reponse == JOptionPane.YES_OPTION) {
                cs.delete(cs.findById(id_client));
                load();
                vider();
                JOptionPane.showMessageDialog(this, "Client supprimé avec succès !");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur lors de la suppression : " + e.getMessage(),
                    "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_DeleteClientActionPerformed

    private void listeclientsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listeclientsMouseClicked
        // TODO add your handling code here:
        try {
            id_client = Integer.parseInt(model.getValueAt(listeclients.getSelectedRow(), 0).toString());
            nomtxt.setText(model.getValueAt(listeclients.getSelectedRow(), 1).toString());
            prenomtxt.setText(model.getValueAt(listeclients.getSelectedRow(), 2).toString());
            telephonetxt.setText(model.getValueAt(listeclients.getSelectedRow(), 3).toString());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur lors de la sélection : " + e.getMessage(),
                    "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_listeclientsMouseClicked

    private void vider() {
        nomtxt.setText("");
        prenomtxt.setText("");
        telephonetxt.setText("");

    }

