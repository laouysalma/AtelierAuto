    public void load() {
        try {
            model.setRowCount(0);
            for (Client c : cs.findAll()) {
                model.addRow(new Object[]{
                    c.getId_client(),
                    c.getNom(),
                    c.getPrenom(),
                    c.getTelephone()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur lors du chargement des clients : " + e.getMessage(),
                    "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }

