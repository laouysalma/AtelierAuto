    private void loadImmatricules() {
        try {
            cmbImmatricule.removeAllItems();
            List<Vehicule> list = vs.findAll();

            for (Vehicule v : list) {
                cmbImmatricule.addItem(v.getImmatricule());
            }
        } catch (Exception e) {
            // Gestion d'erreur
        }
    }

    private void loadReparations() {
        DefaultTableModel model = (DefaultTableModel) tableReparations.getModel();
        model.setRowCount(0); // Vider la table

        try {
            List<Reparation> list = rs.findAll();

            for (Reparation r : list) {
                model.addRow(new Object[]{
                    r.getId_reparation(),
                    r.getType(),
                    r.getCout(),
                    r.getDate(),
                    r.getImmatricule()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur lors du chargement des réparations : " + e.getMessage(),
                    "Erreur", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
