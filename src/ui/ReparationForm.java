    private void tableReparationsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableReparationsMouseClicked
        int row = tableReparations.getSelectedRow();

        if (row != -1) {
            try {
                txtType.setText(tableReparations.getValueAt(row, 1).toString());
                txtCout.setText(tableReparations.getValueAt(row, 2).toString());

                Object dateObj = tableReparations.getValueAt(row, 3);
                if (dateObj instanceof Date) {
                    dateChooser.setDate((Date) dateObj);
                }

                String immat = tableReparations.getValueAt(row, 4).toString();
                cmbImmatricule.setSelectedItem(immat);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erreur lors du chargement des données : " + e.getMessage(),
                        "Erreur", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
       }//GEN-LAST:event_tableReparationsMouseClicked

    private void btnHistoriqueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHistoriqueActionPerformed
        if(cmbImmatricule.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this,
                "Veuillez sélectionner un véhicule !",
                "Aucune sélection",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            String immatricule = cmbImmatricule.getSelectedItem().toString();
            List<Reparation> reparations = rs.findByVehicule(immatricule);

            if(reparations.isEmpty()) {
                JOptionPane.showMessageDialog(this,
                    "Aucune réparation trouvée pour le véhicule : " + immatricule,
                    "Aucun résultat",
                    JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            JDialog dialogHistorique = new JDialog();
            dialogHistorique.setTitle("Historique - " + immatricule);
            dialogHistorique.setModal(true);
            dialogHistorique.setSize(800, 500);
            dialogHistorique.setLocationRelativeTo(this);
            dialogHistorique.setDefaultCloseOperation(javax.swing.WindowConstants.DISPOSE_ON_CLOSE);

            String[] colonnes = {"ID", "Type", "Coût (MAD)", "Date"};
            DefaultTableModel modelHistorique = new DefaultTableModel(colonnes, 0) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };

            JTable tableHistorique = new JTable(modelHistorique);
            tableHistorique.setFont(new java.awt.Font("Segoe UI", 0, 14));
            tableHistorique.setRowHeight(25);
            tableHistorique.getTableHeader().setFont(new java.awt.Font("Segoe UI", java.awt.Font.BOLD, 14));

            double coutTotal = 0;
            for(Reparation r : reparations) {
                modelHistorique.addRow(new Object[]{
                    r.getId_reparation(),
                    r.getType(),
                    String.format("%.2f", r.getCout()),
                    r.getDate()
                });
                coutTotal += r.getCout();
            }

            JPanel panelPrincipal = new JPanel(new java.awt.BorderLayout(10, 10));
            panelPrincipal.setBorder(javax.swing.BorderFactory.createEmptyBorder(10, 10, 10, 10));

            JScrollPane scrollPane = new JScrollPane(tableHistorique);
            panelPrincipal.add(scrollPane, java.awt.BorderLayout.CENTER);

            JPanel panelTotal = new JPanel(new java.awt.FlowLayout(java.awt.FlowLayout.RIGHT));
            panelTotal.setBackground(new java.awt.Color(255, 255, 255));

            JLabel lblTotal = new JLabel("Total des coûts : " + String.format("%.2f", coutTotal) + " MAD");
            lblTotal.setFont(new java.awt.Font("Segoe UI", java.awt.Font.BOLD, 18));
            lblTotal.setForeground(new java.awt.Color(255, 0, 0));
            panelTotal.add(lblTotal);

            panelPrincipal.add(panelTotal, java.awt.BorderLayout.SOUTH);

            JPanel panelHaut = new JPanel(new java.awt.FlowLayout(java.awt.FlowLayout.RIGHT));
            JButton btnFermer = new JButton("✕");
            btnFermer.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 20));
            btnFermer.setFocusPainted(false);
            btnFermer.setBorderPainted(false);
            btnFermer.setContentAreaFilled(false);
            btnFermer.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
            btnFermer.addActionListener(e -> dialogHistorique.dispose());
            panelHaut.add(btnFermer);

            panelPrincipal.add(panelHaut, java.awt.BorderLayout.NORTH);

            dialogHistorique.add(panelPrincipal);
            dialogHistorique.setVisible(true);

        } catch(Exception e) {
            JOptionPane.showMessageDialog(this,
                "Erreur lors du chargement de l'historique : " + e.getMessage(),
                "Erreur",
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnHistoriqueActionPerformed


