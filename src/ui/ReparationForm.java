    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // Validation des champs
        if (txtType.getText().trim().isEmpty()
                || txtCout.getText().trim().isEmpty()
                || dateChooser.getDate() == null
                || cmbImmatricule.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs !",
                    "Champs manquants", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            String type = txtType.getText().trim();
            double cout = Double.parseDouble(txtCout.getText().trim());
            Date date = dateChooser.getDate();
            String immatricule = cmbImmatricule.getSelectedItem().toString();

            if (cout <= 0) {
                JOptionPane.showMessageDialog(this, "Le coût doit être supérieur à 0 !",
                        "Erreur de validation", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Reparation r = new Reparation(type, cout, date, immatricule);

            if (rs.create(r)) {
                JOptionPane.showMessageDialog(this, "Réparation ajoutée avec succès !",
                        "Succès", JOptionPane.INFORMATION_MESSAGE);
                vider();
                loadReparations();
            } else {
                JOptionPane.showMessageDialog(this, "Erreur lors de l'ajout de la réparation !",
                        "Erreur", JOptionPane.ERROR_MESSAGE);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Le coût doit être un nombre valide !",
                    "Erreur de format", JOptionPane.ERROR_MESSAGE);
            txtCout.requestFocus();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur : " + e.getMessage(),
                    "Erreur", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int row = tableReparations.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner une réparation à modifier !",
                    "Aucune sélection", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (txtType.getText().trim().isEmpty()
                || txtCout.getText().trim().isEmpty()
                || dateChooser.getDate() == null
                || cmbImmatricule.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs !",
                    "Champs manquants", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            int id = (int) tableReparations.getValueAt(row, 0);

            String type = txtType.getText().trim();
            double cout = Double.parseDouble(txtCout.getText().trim());
            Date date = dateChooser.getDate();
            String immatricule = cmbImmatricule.getSelectedItem().toString();

            if (cout <= 0) {
                JOptionPane.showMessageDialog(this, "Le coût doit être supérieur à 0 !",
                        "Erreur de validation", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Reparation r = new Reparation(id, immatricule, date, type, cout);

            if (rs.update(r)) {
                JOptionPane.showMessageDialog(this, "Réparation modifiée avec succès !",
                        "Succès", JOptionPane.INFORMATION_MESSAGE);
                vider();
                loadReparations();
            } else {
                JOptionPane.showMessageDialog(this, "Erreur lors de la modification !",
                        "Erreur", JOptionPane.ERROR_MESSAGE);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Le coût doit être un nombre valide !",
                    "Erreur de format", JOptionPane.ERROR_MESSAGE);
            txtCout.requestFocus();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur : " + e.getMessage(),
                    "Erreur", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = tableReparations.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner une réparation à supprimer !",
                    "Aucune sélection", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String type = tableReparations.getValueAt(row, 1).toString();
        String immatricule = tableReparations.getValueAt(row, 4).toString();

        int confirm = JOptionPane.showConfirmDialog(this,
                "Êtes-vous sûr de vouloir supprimer cette réparation ?\n\n"
                + "Type: " + type + "\n"
                + "Immatricule: " + immatricule,
                "Confirmation de suppression",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                int id = (int) tableReparations.getValueAt(row, 0);

                Reparation r = rs.findById(id);

                if (r != null && rs.delete(r)) {
                    JOptionPane.showMessageDialog(this, "Réparation supprimée avec succès !",
                            "Succès", JOptionPane.INFORMATION_MESSAGE);
                    vider();
                    loadReparations();
                } else {
                    JOptionPane.showMessageDialog(this, "Erreur lors de la suppression !",
                            "Erreur", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erreur : " + e.getMessage(),
                        "Erreur", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

